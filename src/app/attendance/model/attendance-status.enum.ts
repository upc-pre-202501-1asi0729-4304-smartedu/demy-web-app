/**
 * Enum representing the possible attendance statuses for a student.
 * Used to indicate whether a student was present or absent in a class session.
 */
export enum AttendanceStatus {
  /**
   * Indicates the student was present.
   */
  PRESENT = 'PRESENT',

  /**
   * Indicates the student was absent.
   */
  ABSENT = 'ABSENT',
}
